# Variablen definieren
a = 10
b = 3

# Berechnungen
summe = a + b
differenz = a - b
produkt = a * b
quotient = a // b  # Ganzzahliger Quotient
rest = a % b  # Rest der Division
exponential = a ** b  # a hoch b

# Ausgabe der Ergebnisse
